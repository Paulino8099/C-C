import { useContext } from 'react';
import { DatosContext } from '../../../../hooks/storeProvider';
import { ApartadoCintas } from '../../apartado-cintas/ApartadoCintas';

//?????????????????????????????????????????????????????????????????
//?????????????????????????????????????????????????????????????????
//?????????????????????????????????????????????????????????????????

export const Apartado = () => {
	const { datos } = useContext(DatosContext);

	return (
		(datos.identificadorApartado === 'Cintas' && <ApartadoCintas />) ||
		(datos.identificadorApartado === 'Cintillos' && <h1>Cintillos</h1>) ||
		(datos.identificadorApartado === 'Vendas' && <h1>Vendas</h1>) ||
		(datos.identificadorApartado === 'Ventas' && <h1>Ventas</h1>) ||
		(datos.identificadorApartado === 'Clientes' && <h1>Clientes</h1>)
	);
};
