export const BtnNo = () => {
	const { datos, setDatos } = useContext(DatosContext);


	const variants = {
		visible: {
			'align-items': 'center',
			'align-self': 'flex-end',
			borderRadius: '15px',
			'justify-content': 'space-evenly',
			justifySelf: 'center',
			'margin-bottom': '3%',
			'margin-left': '-65px',
			background: 'var(--color-1)',
			color: '#ff0000',
			height: datos.modalConfirmacion ? '20px' : 0,
			padding: datos.modalConfirmacion ? '3px 5px' : 0,
			position: 'absolute',
			transform: 'scale(1)',
			transition: { duration: 0.1 },
			width: datos.modalConfirmacion ? '50px' : 0,
            fontSize: '18px',

			display: 'flex'
		},
		tap: {
			transform: 'scale(0.9)',
			transition: { duration: 0.1 }
		}
	};

	return (
		<motion.div
			variants={variants}
			initial="hidden"
			whileTap="tap"
			animate="visible"
			onClick={() => {
				setDatos({
					...datos,
					modalConfirmacion: false,
					fondoOpacado: false
				});
			}}
		>
			<TiDelete />
			<h3>NO</h3>
		</motion.div>
	);
};
