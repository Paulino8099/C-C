import { useContext } from 'react';
import { DatosContext } from '../../../hooks/storeProvider';
import { TiDelete } from 'react-icons/ti';
import { motion } from 'framer-motion';

//?????????????????????????????????????????????????????????????????
//?????????????????????????????????????????????????????????????????
