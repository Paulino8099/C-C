export const BtnMenuColores = ({ idCarpeta, idEspecialCarpeta }) => {
	const { datos, setDatos } = useContext(DatosContext);

	const variants = {
		hidden: {},
		visible: {
			border: '2px solid var(--color-1)',
			borderRadius: '50px',
			color: 'var(--color-1)',
			height: '22px',
			margin: '3%',
			placeItems: 'center',
			position: 'absolute',
			transform: 'scale(1)',
			transition: { duration: 0.01 },
			width: '22px',
			zIndex: '150',

			display: 'grid'
		},
		tap: {
			transform: 'scale(0.9)',
			transition: { duration: 0.1 }
		}
	};

	return (
		<motion.span
			variants={variants}
			initial="hidden"
			animate="visible"
			whileTap="tap"
			onClick={() => {
				setDatos({
					...datos,
					modalMenuColores: !datos.modalMenuColores,
					fondoOpacado: true,
					idCarpetaSeleccionada: idCarpeta,
				});
			}}
		>
			<BiPaintRoll />
		</motion.span>
	);
};
