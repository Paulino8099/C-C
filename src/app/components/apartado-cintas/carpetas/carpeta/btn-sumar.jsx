export const BtnSumar = ({ idCarpeta }) => {
	const { datos, setDatos } = useContext(DatosContext);

	const variants = {
		hidden: {},
		visible: {
			'align-self': 'flex-end',
			borderRadius: '15px',
			justifySelf: 'center',
			'margin-bottom': '3px',
			'margin-right': '-25px',
			placeItems: 'center',
			background: 'linear-gradient(90deg, var(--color-4), var(--color-6))',
			color: 'var(--color-1)',
			height: '20px',
			padding: '2px',
			position: 'absolute',
			transform: 'scale(1)',
			transition: { duration: 0.1, ease: 'easeInOut' },
			width: '20px',

			display: 'grid'
		},
		tap: {
			background: 'linear-gradient(0deg, var(--color-4), var(--color-6))',
			transform: 'scale(0.9)',
			transition: { duration: 0.1, ease: 'easeInOut' }
		}
	};

	function sumandoCantidad() {
		const seleccionandoCarpeta = datos.carpetas[idCarpeta];

		const cambiandoCantidad = {
			...seleccionandoCarpeta,
			cantidad: seleccionandoCarpeta.cantidad + 1
		};

		datos.carpetas.splice(idCarpeta, 1, cambiandoCantidad);

		setDatos({
			...datos,
			actualizacionData: ''
		});
	}

	return (
		<motion.span variants={variants} key={idCarpeta} initial="hidden" animate="visible" whileTap="tap">
			<MdAddCircleOutline
				onClick={() => {
					// envioDatosCantidad();
					sumandoCantidad();
				}}
			/>
		</motion.span>
	);
};
